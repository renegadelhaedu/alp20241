def soma(num1, num2):
    if(num1 > 0 and num2 > 0):
        return num1 + num2
    else:
        return 'numeros negativos'

def sub(num1, num2):
    if(num1 > 0 and num2 > 0):
        return num1 - num2
    else:
        return 'numeros negativos'

def multi(num1, num2):
    if(num1 > 0 and num2 > 0):
        return num1 * num2
    else:
        return 'numeros negativos'

def div(num1, num2):
    if(num1 > 0 and num2 > 0):
        return num1 / num2
    else:
        return 'numeros negativos'
